  static List<WaterTestReport> latestByLocation() {
    final latestReports = <String, WaterTestReport>{};

    for (final report in all) {
      final current = latestReports[report.locationId];
      if (current == null || report.testedAt.isAfter(current.testedAt)) {
        latestReports[report.locationId] = report;
      }
    }

    return List<WaterTestReport>.unmodifiable(latestReports.values);
  }

  static List<WaterTestReport> forLocation(String locationId) {
    final reports = all
        .where((report) => report.locationId == locationId)
        .toList(growable: false)
      ..sort((a, b) => b.testedAt.compareTo(a.testedAt));

    return List<WaterTestReport>.unmodifiable(reports);
  }
